int ft_isalpha (int c)
{
    if ((64 < c && c < 91 )|| (96 < c && c < 123))
        return (1);
    else
        return 0;
}

int main()
{
    unsigned char x;
    x = 'A';
    ft_isalpha(x);
    return 0;
}